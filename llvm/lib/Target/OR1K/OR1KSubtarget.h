#ifndef LLVM_LIB_TARGET_OR1K_OR1KSUBTARGET_H
#define LLVM_LIB_TARGET_OR1K_OR1KSUBTARGET_H

#include "MCTargetDesc/OR1KBaseInfo.h"
#include "OR1KFrameLowering.h"
#include "OR1KISelLowering.h"
#include "OR1KInstrInfo.h"
#include "OR1KRegisterInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "OR1KGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class OR1KSubtarget : public OR1KGenSubtargetInfo {
  virtual void anchor();
  bool HasLA64 = false;
  bool HasBasicF = false;
  bool HasBasicD = false;
  bool HasExtLSX = false;
  bool HasExtLASX = false;
  bool HasExtLVZ = false;
  bool HasExtLBT = false;
  unsigned GRLen = 32;
  MVT GRLenVT = MVT::i32;
  OR1KABI::ABI TargetABI = OR1KABI::ABI_Unknown;
  OR1KFrameLowering FrameLowering;
  OR1KInstrInfo InstrInfo;
  OR1KRegisterInfo RegInfo;
  OR1KTargetLowering TLInfo;

  /// Initializes using the passed in CPU and feature strings so that we can
  /// use initializer lists for subtarget initialization.
  OR1KSubtarget &initializeSubtargetDependencies(const Triple &TT,
                                                 StringRef CPU,
                                                 StringRef TuneCPU,
                                                 StringRef FS,
                                                 StringRef ABIName);

public:
  // Initializes the data members to match that of the specified triple.
  OR1KSubtarget(const Triple &TT, StringRef CPU, StringRef TuneCPU,
                StringRef FS, StringRef ABIName, const TargetMachine &TM);

  // Parses features string setting specified subtarget options. The
  // definition of this function is auto-generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const OR1KFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const OR1KInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const OR1KRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const OR1KTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  bool is64Bit() const { return HasLA64; }
  bool hasBasicF() const { return HasBasicF; }
  bool hasBasicD() const { return HasBasicD; }
  bool hasExtLSX() const { return HasExtLSX; }
  bool hasExtLASX() const { return HasExtLASX; }
  bool hasExtLVZ() const { return HasExtLVZ; }
  bool hasExtLBT() const { return HasExtLBT; }
  MVT getGRLenVT() const { return GRLenVT; }
  unsigned getGRLen() const { return GRLen; }
  OR1KABI::ABI getTargetABI() const { return TargetABI; }
};
} // namespace llvm

#endif // LLVM_LIB_TARGET_OR1K_OR1KSUBTARGET_H
